// Type for custom tokens - using any for flexibility
// eslint-disable-next-line @typescript-eslint/no-explicit-any
export type TokensConfig = any;
export type DeepPartial<T> = {
  [P in keyof T]?: T[P] extends object ? DeepPartial<T[P]> : T[P];
};

// Global tokens instance
let tokensInstance: TokensConfig = { ...defaultTokens };

/**
 * Configure custom design tokens
 * @param customTokens - Custom tokens to merge with defaults
 * @param replaceDefaults - If true, completely replace defaults. If false, merge with defaults
 */
export function configureTokens(
  customTokens: DeepPartial<TokensConfig>,
  replaceDefaults = false
): void {
  if (replaceDefaults) {
    tokensInstance = customTokens as TokensConfig;
  } else {
    tokensInstance = deepMerge(defaultTokens, customTokens);
  }
}

/**
 * Get current tokens configuration
 */
export function getTokens(): TokensConfig {
  return tokensInstance;
}

/**
 * Reset tokens to defaults
 */
export function resetTokens(): void {
  tokensInstance = { ...defaultTokens };
}

/**
 * Check if custom tokens are configured
 */
export function hasCustomTokens(): boolean {
  return tokensInstance !== defaultTokens;
}

// Deep merge utility
function deepMerge<T>(target: T, source: DeepPartial<T>): T {
  const result = { ...target } as T;

  for (const key in source) {
    const sourceValue = source[key];
    if (
      sourceValue &&
      typeof sourceValue === 'object' &&
      !Array.isArray(sourceValue)
    ) {
      // eslint-disable-next-line @typescript-eslint/no-explicit-any
      (result as any)[key] = deepMerge((result as any)[key], sourceValue);
    } else if (sourceValue !== undefined) {
      // eslint-disable-next-line @typescript-eslint/no-explicit-any
      (result as any)[key] = sourceValue;
    }
  }

  return result;
}

